# --- Python Script: Weather Decision ---

# Let's imagine we have a way to know if it's raining (for simplicity, we'll just set it)
is_raining = True # Try changing this to False and run the script again!

print("Time to decide what to wear...")

# Here's our if/else statement!
if is_raining == True: # Check if the variable 'is_raining' is exactly True
    print("It's raining! Don't forget your umbrella and rain boots!")
else: # If the condition above (is_raining == True) is NOT true (meaning it's False)
    print("It's not raining! Grab your sunglasses and a hat!")

print("Have a great day!")

# --- End of Weather Decision Script ---